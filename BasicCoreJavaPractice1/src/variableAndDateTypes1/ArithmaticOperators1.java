public class ArithmaticOperators1 {

	public static void main(String[] args)
	{
		int a=15;
		int b=30;
		
		
		int sum=a+b;
		System.out.println("ADD = "+sum);
		
		int sub=b-a;
		System.out.println("sub = " +sub);
		
		
		int div=b/a;
		System.out.println("div = " +div);
				
														
		int mul=a*b;
		System.out.println(mul);
		
		
		int mod=a%b;
		System.out.println(mod);
		

	}

}
